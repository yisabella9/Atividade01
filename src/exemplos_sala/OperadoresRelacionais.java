package exemplos_sala;

public class OperadoresRelacionais {

    public static void main(String[] args) {
        int a = 10, b = 3;

        System.out.println("\n=== OPERADORES RELACIONAIS ===");
        System.out.println("a > b? " + (a > b));
        System.out.println("a < b? " + (a < b));
        System.out.println("a == b? " + (a == b));
        System.out.println("a != b? " + (a != b));
    }
}